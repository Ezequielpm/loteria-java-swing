public class EstadisticasPerfil {
    int idJugador;
    String nombreJugador;
    int partidasTotales, partidasPerdidas, partidasGanadas;
    int puntosEasy, puntosMedium, puntosHard;

    public int getIdJugador() {
        return idJugador;
    }

    public void setIdJugador(int idJugador) {
        this.idJugador = idJugador;
    }

    public String getNombreJugador() {
        return nombreJugador;
    }

    public void setNombreJugador(String nombreJugador) {
        this.nombreJugador = nombreJugador;
    }

    public int getPartidasTotales() {
        return partidasTotales;
    }

    public void setPartidasTotales(int partidasTotales) {
        this.partidasTotales = partidasTotales;
    }

    public int getPartidasPerdidas() {
        return partidasPerdidas;
    }

    public void setPartidasPerdidas(int partidasPerdidas) {
        this.partidasPerdidas = partidasPerdidas;
    }

    public int getPartidasGanadas() {
        return partidasGanadas;
    }

    public void setPartidasGanadas(int partidasGanadas) {
        this.partidasGanadas = partidasGanadas;
    }

    public int getPuntosEasy() {
        return puntosEasy;
    }

    public void setPuntosEasy(int puntosEasy) {
        this.puntosEasy = puntosEasy;
    }

    public int getPuntosMedium() {
        return puntosMedium;
    }

    public void setPuntosMedium(int puntosMedium) {
        this.puntosMedium = puntosMedium;
    }

    public int getPuntosHard() {
        return puntosHard;
    }

    public void setPuntosHard(int puntosHard) {
        this.puntosHard = puntosHard;
    }
    
    
    
}
